package com.example;

import java.util.List;

public class Animal {

    // Метод getFood() возвращает список еды в зависимости от типа животного (Травоядное или Хищник)
    public List<String> getFood(String animalKind) throws Exception {
        // Если передан тип животного "Травоядное"
        if ("Травоядное".equals(animalKind)) {
            // Возвращаем список еды для травоядного
            return List.of("Трава", "Различные растения");
        }
        // Если передан тип животного "Хищник"
        else if ("Хищник".equals(animalKind)) {
            // Возвращаем список еды для хищника
            return List.of("Животные", "Птицы", "Рыба");
        }
        // Если тип животного неизвестен, выбрасывается исключение
        else {
            // Выбрасываем исключение с сообщением об ошибке
            throw new Exception("Неизвестный вид животного, используйте значение Травоядное или Хищник");
        }
    }

    // Метод getFamily() возвращает описание различных семейств животных
    public String getFamily() {
        return "Существует несколько семейств: заячьи, беличьи, мышиные, кошачьи, псовые, медвежьи, куньи";
    }
}
